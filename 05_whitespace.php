<p>Մի անգամ մայրը Կարմիր Գլխարկին ուղարկեց անտառում ապրող տատիկին կարկանդակներ տանելու։ Ճանապարհին աղջիկը գայլի հանդիպեց։ Հարց․ այդ որ մայրը կուղարկեր փոքրիկ աղջկան անտառ, ուր գայլեր են ապրում։ Ինչու՞ նա ինքը չտարավ ուտելիքը, կամ չգնաց աղջկա հետ։ Եթե տատիկն այդքան անօգնական էր, ինչու՞ էր մայրը թողել նրան միայնակ ապրել հեռավոր խրճիթում։ Եւ եթե աղջիկն այնուամենայնիվ պետք է գնար, ինչու՞ մայրը նրան չի արգելում խոսել գայլերի հետ։ Այս պատմությունից կարելի է ենթադրել, որ Կարմի Գլխարկին ոչ ոք հավանական վտանգի մասին չէր զգուշացրել։</p>

<p>Ոչ մի մայր իրական կյանքում այսքան անհոգ չէր լինի, այդ իսկ պատճառով տպավորություն է ստեղծվում, որ աղջիկը բացարձակ չէր հետաքրքրում նրան կամ որ մայրը ցանկանում էր նրանից ազատվել։</p>

<p>Միեւնույն ժամանակ դժվար թե երբեւէ գտնվի մեկ այլ այսքան հիմար փոքրիկ աղջիկ։ Ինչպե՞ս կարող էր նա, տեսնելով գայլի աչքերը, ականջները, թաթերն ու ժանիքները, մտածել, որ դիմացն իր տատիկն է։ Ինչու՞ միանգամից չփախնել։ Եւ այդ ի՞նչ փոքրիկ աղջիկ էր, որ հետո օգնում էր գայլի փորը քարերով լցնել։ Համենայն դեպս, ցանկացած լավ աղջիկ գայլի հետ զրուցելուց հետո ծաղիկներ չէր հավաքի եւ կմտածեր․ «սա հաստատ իմ տատիկին ուզում է ուտել, արագ վազեմ օգնության հետեւից»։</p>

<p>Նույնիսկ տատիկն ու որսորդը զերծ չեն կասկածանքներից։ Եթե այս պատմությանը նայենք որպես իրական հերոսներով դրամայի, ապա կնկատենք, թե ինչպիսի կոկիկությամբ են նրանց անհատականությունները միմյանց հետ կապվում։</p>

<p>ա․ մայր, ով հավանաբար ձգտում է ազատվել աղջկանից «դժբախտ պատահարի» միջոցով, որպեսզի պատմության վերջում կարողանա բողոքել․ «միթե ահավոր չէ՞, նույնիսկ անտառ չես կարող գնալ, առանց մտածելու, որ ինչ֊որ մի գայլ․․․»</p>

<p>բ․ գայլ, ով նապաստակներ եւ այլ կենդանիներ ուտելու փոխարեն միանշանակ իր հնարավորություններից բարձր է ապրում։ Նա կարող էր գուշակել, որ ինքն իր գլխին փորձանք է բերում եւ որ վերջը վատ է լինելու ։ Նա հավանաբար երիտասարդ ժամանակ Նիցշե էր կարդացել (եթե նա կարողանում է խոսել եւ գլխարկ գործել, ինչու՞ չկարդալ)։ Գայլի կարգախոսն է․ ապրիր վտանգներով լի եւ մահացիր փառքով։</p>

<p>գ․ տատիկ, ով ապրում է մենակ, սակայն չգիտես ինչու դուռը փակ չի պահում։ Նա հավանաբար ինչ֊որ արկածների հույս ունի, այնպիսի մի բանի, ինչ չէր կարող լինել, եթե նա բարեկամների հետ ապրեր։ Գուցե հենց այդ պատճառով էլ նա չի ցանկանում միասին կամ նրանց հարեւանությամբ ապրել։ Տատիկը բավականին երիտասարդ կին է թվում, չէ որ նա այդքան փոքր թոռնուհի ունի։ Իսկ ինչու՞ արկածներ չփնտրել։</p>

<p>դ․ որսորդ, ով, հավանաբար, այն փրկարարներից է, ում դուր է գալիս փոքրիկ աղջիկների հետ միասին պարտված հակառակորդին պատժել։ Մեր դիմաց ակհայտ դեռահասական պատկեր է։</p>

<p>ե․ Կարմիր Գլխարկ․ գայլին հայտնում է, թե որտեղ կարող են նորից հանդիպել, եւ անգամ նրա ծոցն է մտնում։ Նա բացահայտ խաղեր է խաղում գայլի հետ։ Նրա համար այդ խաղն այս անգամ հաջող ավարտ է ունենում։</p>

<p>Այս հեքիաթում յուրաքանչյուր հերոս ձգտում է գործողության ամեն գնով։ Եթե արդյունքը վերցնենք այնպիսին, ինչպես իրականում է, ապա ընդհանուր առմամբ այս ամենը բանսարկություն է, որի ծուղակն ընկնում է դժբախտ գայլը։ Նրան թողել են երեւակայել, որ շատ խորամանկ է եւ կարող է բոլորին ֆռռացնել։ Եւ այդ դեպքում, հեքիաթը իմաստը գուցե նրանում չէ, որ փոքրիկ աղջիկները պետք է հեռու մնան անտառներից, որտեղ գայլեր են ապրում, այլ այն, որ գուցե գայլերին է պետք հեռու մնալ անմեղ թվացող փոքրիկ աղջնակներից եւ նրանց տատիկներց։ Կարճ ասած՝ գայլին չի կարելի անտառով մենակ զբոսնել։ Եւ եւս մեկ հետաքրքիր հարց, որ առաջանում է․ ի՞նչ էր անում մայրը աղջկան մի ամբողջ օրով տատիկի տուն ուղարկելով։</p>

<p>Եթե ընթերցողն այս վերլուծությունը ցինիզմ համարի, ապա խորհուրդ կտանք նրան պատկերացնել Կարմիր Գլխարկին իրական կյանքում։ Գլխավոր պատասխանն այս հարցի մեջ է․ ի՞նչ է դառնալու Կարմիր Գլխարկն ապագայում այսպիսի մորով եւ նման փորձով։</p>
